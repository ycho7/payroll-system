def calculate_malaysian_payroll(
    basic_salary, 
    fixed_allowance, 
    is_malaysian, 
    is_over_60, 
    bonus=0.0, 
    profit_sharing=0.0,
    additional_allowance=0.0  # New Parameter Added
):
    # Sum all relevant wage components for statutory contributions
    # Additional Allowance is added here to ensure it's part of the lookup gross
    gross = (
        basic_salary + 
        fixed_allowance + 
        additional_allowance + 
        bonus + 
        profit_sharing
    )
    
    # 1. EPF Calculation using Mapping (Jadual Ketiga)
    if is_malaysian:
        if is_over_60:
            # Over 60: Employer 6.5%, Employee 5.5%, rounded up to next Ringgit
            epf_er = math.ceil(gross * 0.065)
            epf_ee = math.ceil(gross * 0.055)
        else:
            # Standard: Uses your provided mapping table for exact amounts
            epf_er, epf_ee = get_epf_contribution(gross)
    else:
        # 2026 Foreigner Rule: Mandatory 2% / 2%, rounded up
        epf_er = math.ceil(gross * 0.02)
        epf_ee = math.ceil(gross * 0.02)

    gross_without_bonus = (
        basic_salary +
        fixed_allowance +
        additional_allowance +
        profit_sharing
    )

    # 2. SOCSO (Act 4) Lookup
    # Note: Foreigners are covered under Act 4 since 2019/2020.
    socso_er, socso_ee = get_socso_rates(gross_without_bonus, is_over_60, is_malaysian)

    # 3. EIS (Act 800) Lookup
    # EIS generally applies to Malaysians/PRs only.
    if not is_over_60 and is_malaysian:
        val = get_eis_contribution(gross_without_bonus)
        eis_er, eis_ee = val, val
    else:
        # EIS is RM 0.00 for foreigners or those first joining over 60
        eis_er, eis_ee = 0.0, 0.0

    # Calculate Net Pay (Deductions: EE shares only)
    net_pay = gross - epf_ee - socso_ee - eis_ee

    return {
        "gross": gross,
        "epf_ee": epf_ee, 
        "epf_er": epf_er,
        "socso_ee": socso_ee, 
        "socso_er": socso_er,
        "eis_ee": eis_ee, 
        "eis_er": eis_er,
        "net_pay": net_pay
    }

def calculate_age_from_ic(ic_string, month_year_str=None):
    """
    ic_string: "900101-01-1234"
    month_year_str: Optional "2026-06"
    """
    if not ic_string or len(ic_string) < 6:
        return 0
    
    # 1. Parse target date or default to 'now'
    if month_year_str:
        target_date = datetime.strptime(month_year_str, "%Y-%m")
    else:
        target_date = datetime.now()
        
    target_year = target_date.year
    target_month = target_date.month
    
    # 2. Extract Birth Info from IC
    year_short = int(ic_string[:2])
    birth_month = int(ic_string[2:4])
    
    # 3. Determine Century
    # (If IC year is greater than current year short, it's 1900s)
    target_yy = target_year % 100
    if year_short > target_yy:
        birth_year = 1900 + year_short
    else:
        birth_year = 2000 + year_short
        
    # 4. Initial Age Calculation
    age = target_year - birth_year
    
    # 5. Adjust if birth month hasn't been reached yet
    if target_month < birth_month:
        age -= 1
        
    return age